--create_tables.sql
BEGIN
    EXECUTE IMMEDIATE 'CREATE TABLE CITIES (
        id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        cities CLOB
        )';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;

BEGIN
    EXECUTE IMMEDIATE 'CREATE TABLE COUNTRIES (
        id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name VARCHAR2(255) NOT NULL,
        cities_id NUMBER,
        largest_city VARCHAR2(255),
        CONSTRAINT fk_cities FOREIGN KEY (cities_id) REFERENCES CITIES(id)
        )';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;




